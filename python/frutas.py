# for utilizado para iterar sobre uma sequência como uma lista,uma trupla ou string)

#a variavel fruta assume cada elemento da lista, 
# a linha e empressa separada.

frutas = ["maçã", "banana", "laranja"]

for fruta in frutas:
    print(fruta)

#while repeti um bloco de código

#o loop while é executado enquanto a variavel for menor que 5

#em cada iteração o valor e impresso do contador apartir da instrução contador +=1 
# e interropido quando o valor for 5  

contador = 0

while contador < 5:
    print(contador )
    contador += 1
